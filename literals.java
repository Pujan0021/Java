
public class literals {
    public static void main(String[] args) {
        int a = 10;
        char b = 'F';
        String c = "Fast and Furious";
        boolean d = false;
        float e = 10.22f;
        System.out.println("a: " + a);
        System.out.println("b: " + b);
        System.out.println("c: " + c);
        System.out.println("d: " + d);
        System.out.println("e: " + e);

    }
}
